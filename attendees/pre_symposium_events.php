<h2><a name="microsoft_cloud_computing">Microsoft Cloud Computing Pre-Symposium Workshop</a></h2>
</div></div>
<img src="/sigcse2010/images/clock.png" align="absmiddle" />Wednesday, March 10th, 2010, 1:00pm to 5:00pm

<p>Every few years, a revolution changes the way we use computers. Think of huge mainframes in the 1960s, minicomputers in the 1970s, personal computers in the 1980s and smartphones over the last decade. Cloud computing is poised to be the next big paradigm shift in the computing landscape by bringing together advances in technologies like Virtualization, massive scale computing, communication formats and flexible data protocols. With advances by Microsoft, Amazon and other leaders in computing, this field is slated as the fastest growing segment of the technology industry in terms of investment, development and recruitment.</p> 
 
<p>Join us in the Microsoft Cloud Computing workshop to learn more about this exciting development and about Windows Azure, our internet-scale cloud services platform, which provides an operating system and a rich set of developer services that allow you to build new applications to run from the cloud or enhance existing applications with cloud-based capabilities.</p>

<p>This event is available on a first-come first-served basis to 100 attendees via the SIGCSE 2010 Symposium registration form.
</p>

