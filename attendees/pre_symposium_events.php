<h2><a name="hfoss">Humanitarian Free and Open Source Software (HFOSS) Symposium 2010: "Growing the Humanitarian FOSS Community"</a></h2>
</div></div>
<br/>

<p>This one-day symposium (<a href="www.hfoss.org/hfoss2010">www.hfoss.org/hfoss2010</a>), held as a SIGCSE 2010 pre-conference activity, aims to bring together educators, software developers, industry representatives, and students to continue the discussion begun at SIGCSE 2009.  This year's symposium focuses on how to expand the study of Humanitarian FOSS (HFOSS) so that it reaches many more students, colleges, secondary schools, and software industry representatives throughout the nation.  Humanitarian FOSS is free and open source software that contributes in some way to the public good.</p>

<p>The symposium will be highly interactive, taking place in three plenary sessions and a break-out session. In addition to the keynote address, the symposium will include invited speakers from industry and academia, an open-ended round-table discussion, a poster session, and four break-out discussion groups.</p>

