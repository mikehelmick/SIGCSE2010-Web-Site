<h2><a name="media_computation">Media Computation Pre-Symposium Workshop</a></h2>
</div></div>
<img src="/sigcse2010/images/clock.png" align="absmiddle" />Wednesday, March 10th, 2010, 8:00am to 5:30pm<br/>
