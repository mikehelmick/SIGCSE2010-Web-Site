<h2><a name="computing_with_personal_robots">Introducing Computing with Personal Robots -- SIGCSE 2010 Edition</a></h2>
</div></div>
<img src="/sigcse2010/images/clock.png" align="absmiddle" />Wednesday, March 10th, 2010, 8:00am to 5:00pm

<p>The Institute for Personal Robots in Education (IPRE) is pleased to
offer a pre-symposium hands-on workshop for instructors interested in
using robots as a stimulating context for CS education.  First run as
a series of summer workshops in 2008, this updated pre-symposium will
introduce you to a fresh, but proven practical approach to CS1 that
uses personal robots.  You are highly encouraged to bring your laptop;
however, you will be provided with a personal robot kit and curricular
materials. You will receive hands on training on how to teach CS1 with
personal robots, as well first hand accounts from the broader IPRE
community. The robots are programmed using Python. No prior knowledge
of Python will be essential for attending this pre-symposium. </p>

<p>This event is available on a first-come first-served basis to 40 attendees via the SIGCSE 2010 Symposium registration form.  Additional information can be found at <a href="http://wiki.roboteducation.org/SIGCSE_2010_Pre-Symposium">http://wiki.roboteducation.org/SIGCSE_2010_Pre-Symposium</a>.</p>

