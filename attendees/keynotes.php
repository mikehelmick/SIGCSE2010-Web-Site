<div class="SectionHeader"><div class="Full">
<h2>Sally Fincher</h2>
</div></div>
School of Computing - University of Kent

<div align="center"><h3>Useful Sharing</h3></div>

<p><b>Abstract:</b> In this talk, I'll explore some of the ways educators share details of their practice and of how they find out "what works" from others. This exploration will include examining some barriers and inhibitors to successful exchange and some thoughts on the importance of representations of practice; that is how we represent our teaching to ourselves, to each other and for posterity.
As part of this exploration, I will present a model of sharing I have devised with Josh Tenenberg, called the Disciplinary Commons: further details of this can be seen at: <a href="http://www.disciplinarycommons.org">http://www.disciplinarycommons.org</a>.</p>

<p><b>Bio:</b> Sally Fincher is Professor of Computing Education in the School of Computing at the University of Kent where she leads the Computing Education Research Group. Her work is centrally concerned with the teaching and learning of Computing, with particular emphasis on teachers and teaching practices. She has worked on several major computing education projects, such as the Bootstrapping Research in Computer Science Education series, and currently manages the UK "sharing practice" project: <a href="http://www.sharingpractice.ac.uk">http://www.sharingpractice.ac.uk</a>. She is Editor-in-Chief of the Journal Computer Science Education (jointly with Laurie Murphy), a UK National Teaching Fellow, a Senior Fellow of the UK Higher Education Academy, a Fellow of the Royal Society of Arts and a Distinguished Scientist of the ACM.</p>

<div class="SectionHeader"><div class="Full">
<h2>Carl E. Wieman</h2>
</div></div>
Director of the Carl Wieman Science Education Initiative and Professor of Physics,
University of British Columbia<br>
Director of the Science Education Initiative and Distinguished Professor of Physics,
University of Colorado at Boulder

<div align="center"><h3>Science Education for the 21st Century: Using the Insights of Science to Teach/Learn Science</h3></div>


<p><b>Abstract:</b> Guided by experimental tests of theory and practice, science has advanced rapidly in the past 500 years.  Guided primarily by tradition and dogma, science education meanwhile has remained largely medieval.  Research on how people learn is now revealing how many teachers badly misinterpret what students are thinking and learning from traditional science classes and exams.  However, research is also providing insights on how to do much better.  The combination of this research with modern information technology is setting the stage for a new approach that can provide the relevant and effective science education for all students that is needed for the 21st century.  I will discuss the failures of traditional educational practices, even as used by “very good” teachers, and the successes of some new practices and technology that characterize this more effective approach, and how these results are highly consistent with findings from cognitive science.</p>

<p><b>Bio:</b> Carl Wieman is currently the director of the Carl Wieman Science Education Initiative at the University of British Columbia and a similar program at the University of Colorado at Boulder. These collaborative initiatives are aimed at achieving departmental-wide sustainable improvement in undergraduate science education.   He has carried out research in a variety of areas of atomic physics and laser spectroscopy.  His research has been recognized with numerous awards and honorary degrees including the Nobel Prize in Physics in 2001 for the creation of Bose-Einstein condensation. <br />
<br />
His work in science education has been recognized by being named the US University Professor of the Year in 2004 by the Carnegie Foundation for the Advancement of Higher Education and receiving the National Science Foundation’s Distinguished Teaching Scholar Award in 2001 and the American Association of Physics Teacher’s Oersted Medal in 2007.  He is an elected member of the National Academy of Sciences and serves on the Academy Board on Science Education.  He is also a member of the U.S. National Academy of Education. 
 </p>



<div class="SectionHeader"><div class="Full">
<h2>Michael Wrinn</h2>
</div></div>
Manager, Innovative Software Education - Intel Corporation

<div align="center"><h3>Suddenly, All Computing Is Parallel: Seizing Opportunity Amid the Clamor</h3></div>

<p><b>Abstract:</b> The shift in computing hardware to parallel systems is well underway. Sequential chips are no longer designed, and the proud era of von Neumann architecture passes into history.  Foundational change of this magnitude will disrupt traditional habits throughout the discipline, especially how students are to be introduced to and prepared for new challenges. But prepared how, and for what, exactly?<br>
<br>
With parallel programming models evolving, and a variety of approaches being tried in academia and industry, flexibility is vital. Customary time lags between research findings and classroom experience will have to be shortened, and industrial best practices find a new level of academic pertinence, quickening the pace of adoption for parallel education. In this talk, we’ll look at some of these university/industry collaborations now underway.</p>

<p><b>Bio: </b> Michael Wrinn manages Intel's Innovative Software Education team, which collaborates with universities to bring parallel computing to the mainstream of undergraduate education. He also works with the ACM Education Council to bring industrial perspective to curriculum evolution. Prior Intel roles include managing Intel's software engineering lab in Shanghai, and directing research on human interface technology. He was Intel's representative for the original OpenMP specification, and remains active in the parallel computing community. Before joining Intel, Michael worked at Accelrys, implementing commercial and research simulation codes on a wide variety of parallel/HPC systems. He holds a B.Sc. and Ph.D. (in quantum mechanics) from McGill University.</p>


<div class="SectionHeader"><div class="Full">
<h2>Peter J. Denning</h2>
</div></div>

<div align="center"><h3>What is Computation?</h3> <i>First timer's lunch</i></div>

<p>This innocent looking question hides in the background when we discuss
who we are and what we do.   Just look at all the difficulty we are having
defining "computational thinking".  This is a question that has never been completely
settled, and probably will never be.  The value comes from grappling with it,
not from settling it.  I will review the changing answers our field has offered
to this question since the 1930s.  I will discuss three reasons why our current
answer is insufficient.  I will argue that a definition basing computation on
representations rather than algorithms may relieve our muddle around this.</p>

