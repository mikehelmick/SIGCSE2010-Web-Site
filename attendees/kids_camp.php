<h2><a name="faq">Frequently Asked Questions</a></h2>
</div></div> 

<ol>
 <li><b>How old/young can my child(ren) be to register?</b>
   <p>Care will be provided for children ages 6 months to 12 yrs. If you have older children who want to attend, please contact us.</p></li> 
   
 <li><b>How much does it cost?</b>
   <p>This year the charges will be by session rather than by time: <br />
    Children 6 months - 2 years: 	$35 per child (half day), or $56 per child (full day) <br />
    Children 3 - 12 years: 	$30 per child (half day), or $48 per child (full day) 
   </p></li>
   
 <li><b>Where will Kids Camp be located?</b>
   <p>Kids Camp will be held in the Milwaukee room on the second floor of the Hyatt Regency Hotel. The Convention Center is directly connected to the Regency Hyatt via heated skywalk, which means that visiting your child(ren) will be easy, and at the same time the kids will be away from the conference traffic flows. Restrooms are located adjacent to the room. </p></li>
   
 <li><b>What will my child(ren) be doing while I am attending the conference?</b> 
   <p>SIGCSE Kid Campers will participate in drama, play, and arts &amp; crafts activities. Older kids can participate in our planned computing activities. We have scheduled activity sessions with CS Unplugged, Scratch, and Alice. Student Volunteers will help with the computing activities.</p>
   <p>Our planned activities also include: <ul>
    <li>Computing activities using Microsoft's new Kodu project </li>
    <li>Scavenger hunts on both Thursday and Friday </li>
    <li>Daily activites based on our "Space Explorer" theme </li></ul> 
Additional computing activities are being planned; please check back! </p></li>

 <li><b>What will my children be served for lunch? </b>
   <p>Lunch menu selections are still in planning.  Last year's camp menu included entrees of chicken fingers &amp; fries, grilled cheese  &amp; fries, macaroni  &amp; cheese, and pizza (with 1 topping). Side dishes were celery  &amp; carrot sticks, fruit cup, and a selection of whole fruit (apple, banana, orange)</p></li> 
   
  <li><b>Do I need to bring anything for my child(ren)?</b>
   <p>Yes, a change of clothes is strongly recommended for all children. In addition, parents of young children will need to provide any diapers, wipes, baby food, and/or formula and bottles that may be needed.</p></li> 
   
  <li><b>How many childcare providers will be in the rooms?</b>
   <p>Daycare provider ratios will be 1:2 for kids ages 6 months-11 months, 1:3 for kids 1 to 2 years old, 1:5 for ages 3-5  and  1:7 for 6-12 years old.. Student volunteers for the computing activities will be additional helpers and are not included in this ratio. </p>
   </li>
 </ol>

