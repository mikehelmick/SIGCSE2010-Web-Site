<div class="SectionHeader"><div class="Full">
<h2><a name="familyFun">Family Fun/Museums</a></h2>
</div></div>

<ul>
<li>  <a href=http://www.wisconsinregional.com/agenda.htm id=kf8o title="FIRST Robotics">FIRST Robotics</a>&nbsp; Competition is holding their Wisconsin Regional March 11-13 at the Cellular One Arena, <i><b>across the street</b> </i>from the convention center. &nbsp;Admission is free.</li>
<li>  <a href=http://www.mam.org/ id=vc:w title="Milwaukee Art Museum">Milwaukee Art Museum</a>, one mile east of the convention center, features four floors of over forty art galleries, regularly rotated through an enormous collection of work ranging from antiquity to the present.&nbsp; Includes the Calatrava-designed <a href=http://www.mam.org/info/details/quadracci.php id=th:u title="Quadracci Pavilion">Quadracci Pavilion</a> with a spectacular view over the shores of Lake Michigan. </li>
<li>  <a href=http://www.mpm.edu/ id=k.5l title="Milwaukee Public Museum">Milwaukee Public Museum</a>, only two block from the convention center, is a natural and human history museum.&nbsp; Includes an <a href=http://www.mpm.edu/imax/ id=of4q title="IMAX theater">IMAX theater</a>, <a href=http://www.mpm.edu/planetarium/ id=fm-9 title=Planetarium>Planetarium</a>, many permanent and featured exhibits like <a href=http://www.mpm.edu/exhibitions/permanent/oldmil/ id=abwk title="The Streets of Old Milwaukee">The Streets of Old Milwaukee</a>, and artifacts from Aztalan, Wisconsin's most significant archaeological site.&nbsp; During SIGCSE, MPM will be showing a special temporary exhibit "<a href=http://www.mpm.edu/dead-sea-scrolls/ id=tqe1 title="Dead Sea Scrolls and the Bible">Dead Sea Scrolls and the Bible</a>", the largest such exhibit produced by the museum. </li>
<li>  <a href=http://www.harley-davidson.com/museum id=sioj title="Harley-Davidson Museum">Harley-Davidson Museum</a>, half a mile south of the convention center, is a newly-opened, 130,000-square-foot facility dedicated to 105 years of motorcycle madness. </li>
<li>  <a href=http://www.milwaukeedomes.org/ id=c:80 title="Mitchell Park Horticulture Conservatory Domes">Mitchell Park Horticulture Conservatory Domes</a>, or "The Domes", are Milwaukee's domed gardens, featuring a Tropical Dome, a Desert Dome, and a Show Dome that rotates content seasonally. </li>
<li>  <a href=http://www.milwaukeezoo.org/ id=t8ie title="Milwaukee Public Zoo">Milwaukee Public Zoo</a> is open daily 365 days a year, and features both native and exotic animals from around the world.  </li>
<li>  <a href=http://www.discoveryworld.org/ id=a0m4 title="Discovery World">Discovery World</a> is a 120,000-square foot facility that includes interactive science, technology and freshwater exhibits, learning labs, theaters, television and audio studios, and fresh and saltwater aquariums. </li>
<li>  <a href=http://www.pabstmansion.com/ id=idkg title="The Pabst Mansion">The Pabst Mansion</a> is billed as the "finest flemish revival mansion in America", completed in 1892 by beer baron and philanthropist Frederick Pabst. </li>
<li>   The <a href=http://www.marquette.edu/haggerty/ id=g4bt title="Haggerty Museum of Art">Haggerty Museum of Art</a> on <a href=http://www.mu.edu id=d4k6 title="Marquette University">Marquette University</a> campus 7 blocks west of the convention center, has a permanent collection of over 6,000 paintings, sculptures, photographs and other visual art pieces. </li>
<li>  <a href=http://www.eisnermuseum.org/ id=u9o7 title="Eisner American Museum of Advertising and Design">Eisner American Museum of Advertising and Design</a> is in Milwaukee's historic <a href=http://www.historicthirdward.org/ id=abk0 title="Third Ward">Third Ward</a>, less than a mile southeast of the convention center. </li>
<li><a href="http://www.paradiselanding.com/">Paradise Landing (Waterpark)</a></li>
</ul>      
      
