<h2><a name="what">What Is The Channel SIGCSE Video Exhibition?</a></h2>
</div></div>

            <p>
            What happens each day at SIGCSE after the technical sessions
            are done?  In the past, attendees might have undertaken mundane
            activities such as drinking and dining.  In Chattanooga,
            instead of gathering in restaurants and bars to discuss
            the day’s technical sessions, SIGCSE attendees will be
            gathering in hotel rooms, ordering room service, and watching
            Channel SIGCSE! (It could happen.)
            </p>

            <p>
            What will be on Channel SIGCSE you ask?  You, your students,
            and your animations in the videos you submitted to the
            Channel SIGCSE video exhibition.  Here’s your chance to share
            the dynamic material you haven’t had a good way
            to share - until now. 
            </p>

            <p>
            Where will people see my video?  On Channel SIGCSE, of course.
            Channel SIGCSE will be presented on the closed-circuit video
            of the Chattanooga conference center and conference hotels
            during SIGCSE 2010 in Chattanooga. It is possible the videos
            will be available to attendees on DVD – we’re still working
            out the details.
            </p>

            <p>
            Videos, old and new, are accepted for Channel SIGCSE exhibition.
            For more information on submissions, keep reading.
            </p>


<div class="SectionHeader"><div class="Full">
<h2><a name="kinds">What Kind Of Videos Can I Submit?</a></h2>
</div></div>


            <p>
            We invite videos relevant to computer science education.
            These videos could include classroom lectures, demonstrations,
            or animations.  The videos could be useful in today’s
            classroom or be of historical significance. 
            </p>

            <p>
            To be accepted, a video must be:
            </p>

            <ul>
              <li> relevant to computer science educators
                   </li>
              <li> of reasonable technical quality<sup>*</sup>
                   and running time<sup>**</sup>
                   </li>
              <li> submitted with a permission form from the copyright
                   owner (see permission form below)
                   </li>
            </ul>

            <br />

            <p>
            <sup>*</sup> Reasonable quality:  There are many aspects of
            reasonable quality, and we won’t attempt to list them here;
            however, the panel will know reasonable quality when they
            see it.  We’re not SIGGRAPH and this isn’t Sundance, so flaws
            are accepted (if not expected).
            </p>

            <p>
            <sup>**</sup> Reasonable runtime:  While the running time of
            a video will be a factor in the selection process, there is
            no hard limit for the running time of a video submission.
            As with any video production, you should strive for maximum
            impact in the least amount of time necessary.
            </p>

<div class="SectionHeader"><div class="Full">
<h2><a name="submit">How Do I Submit A Video For Exhibition?</a></h2>
</div></div>

           <ol>
              <li> <strong>Create a video of interest to the SIGCSE
                   community</strong>.
                   </li>
              <li> <strong>Create and post a web-version of the video</strong>.
                   Post the web-version of your video to
                   <a href="http://youtube.com">YouTube.com</a>.
                   The review panel will view your YouTube video.
                   If the video is accepted for publication a high-quality
                   version will be required.
                   </li>
              <li> <strong>Select, or create, a representative image</strong>.
                   The representative images will be used in promotion and,
                   possibly, on the DVD of exhibition videos.  The
                   representative image is typically a frame of the video.
                   It could be the title image, or a particularly memorable
                   image from the video. The representative image must be
                   included in the submission form, so you need the image
                   before completing the submission form.
                   </li>
              <li> <strong>Complete a video proposal submission form</strong>.
                   The submitter will complete the submission form
                   (
                   <a href="/sigcse2010/downloads/SIGCSE2010_VideoSubmissionForm.doc">Word</a>,
                   <a href="/sigcse2010/downloads/SIGCSE2010_VideoSubmissionForm.txt" onclick="target='newwindow'">Text</a>
                   or
                   <a href="/sigcse2010/downloads/SIGCSE2010_VideoSubmissionForm.rtf">RTF</a>
                   )
                   and email the completed form to <script>write_email( "dbouvie", "siue.edu" );</script>.
                   </li>

            </ol>

            <p>
            <strong><i>Deadline:</i></strong>
            All electronic submissions must be <strong>received</strong>
            by <strong class="warning">November 2, 2009</strong>.
            Notifications will be sent by tbd.
            </p>


