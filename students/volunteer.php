<div class="SectionHeader"><div class="Full">
<h2>What do I have to do to become a student volunteer?</h2>
</div></div>  
            <p>
            Each student volunteer is asked to sign up as a volunteer
            prior to the conference.  (Instructions for signing up as
            a student volunteer are given below.)  
            Upon arrival at the conference site, you must check in at the Registration Desk to receive your conference registration packet then visit the Student Volunteer Office to pick up your work schedule (with a list of times, locations, and duties).
            </p>

<div class="SectionHeader"><div class="Full">
<h2>Who is eligible to be a student volunteer?</h2>
</div></div> 
            <p>
            Any full time student may become a student volunteer.
            Both undergraduate and graduate students are welcome.
            You specify your school affiliation when you sign up as
            a volunteer, and you should bring a copy of your current
            course schedule to the conference as verification of your
            full time status.  Students attending the conference for
            the Research Poster competition <!-- or the Doctoral Consortium -->
            are welcome to become volunteers as well.
            </p>

            <p>
            <strong class="boldnote">
            Important information for students requiring a visa:
            </strong>
            The Association for Computing Machinery (ACM, SIGCSE's umbrella
            organization) does not issue formal
            invitation letters for visas.  However, they can issue a visa
            support letter.  For a visa support letter, please send a
            request to 
            <a href="mailto:Bucci@hq.acm.org">Laura Bucci (Bucci@hq.acm.org)</a>
            with the following information:
            </p>

            <ol>
              <li> Name and mailing address as it appears on your passport
                   </li>
              <li> The name of the conference you wish to attend (SIGCSE 2010)
                   </li>
              <li> Registration confirmation number
                   </li>
              <li> If you are the author of any papers accepted for the conference, please provide the title
                   </li>
              <li> Fax number (visa support letters are not issued via email)
                   </li>
            </ol>

<div class="SectionHeader"><div class="Full">
<h2>What do I get in return for being a student volunteer?</h2>
</div></div> 
 
            <p>
            In exchange for 6 hours of conference service, you will receive:
            </p>

            <ul>
              <li> Refunded registration fee.
                   </li>
