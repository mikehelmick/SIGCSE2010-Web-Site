<div class="SectionHeader"><div class="Full">
<h2>What kinds of work will I do as a student volunteer?</h2>
</div></div> 
            <p>
            It varies. You might work at the registration booth to help
            organize the packets that we give out to attendees.
            You might be at the door of the exhibits hall to verify that
            the people entering have a conference badge.
            You might count the participants at various sessions.
            You might hang out at certain sessions to assist the speakers
            with handouts or equipment.
            </p>

<div class="SectionHeader"><div class="Full">
<h2>Do I have a say in what work I do as a student volunteer?</h2>
</div></div> 
            <p>
            Not much, but we'll do what we can.  There are often fewer
            volunteers than we have jobs, so we have to prioritize.
            </p>

<div class="SectionHeader"><div class="Full">
<h2>When during the conference will I do my student volunteer work?</h2>
</div></div> 
            <p>
            This issue is driven by conference needs.
            Some time periods are busier than others.
            We'll take your preferences into account whenever possible.
            When you sign up as a student volunteer, you'll specify
            when you plan to arrive and leave the conference.
            Within those constraints, we'll make some initial plans.
            We realize that things may come up and will try to make
            dynamic changes as needed.
            In return, we ask that you be as flexible as possible.
            Your duties may be broken into separate time periods and even across separate days.  We guarantee no more than 6 hours of assigned work per volunteer but target an average of 5 hours.
            </p>

<div class="SectionHeader"><div class="Full">
<h2>Do I have to be there for the whole conference to be a student volunteer?</h2>
</div></div> 
          <p>
            No. We realize that travel plans and other commitments exist.
            We hope that students local to the Chattanooga area will be
            flexible.  In particular, there is always a need
            for volunteers on Tuesday and Wednesday, which is before most
            of the conference activities begin and before many
            of the traveling student volunteers have arrived.
            We encourage local student volunteers to be available on Tuesday and Wednesday.
            </p>

<div class="SectionHeader"><div class="Full">
<h2>Can I get funding for travel or hotel expenses?</h2>
</div></div> 
            <p>
            The conference itself does not provide funding for student
            volunteers, but you may be able to get some support
            through your department or through another source at your
            university.  You should print your registration
            acknowledgment to show to potential funding sources.
            </p>

