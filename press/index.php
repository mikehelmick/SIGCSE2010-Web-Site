            criteria for media registration.
            </p>

            <p>
            A press card or letter of assignment will be accepted
            as valid press credential.  Without a press card,
            you must verify that you have been retained or assigned
            to cover SIGCSE 2010.  You must fall under one of the
            following categories to receive media registration:
            </p>

            <ul>
              <li> <strong>Business and Trade Publications / News Wire Services</strong>:
                   Reporters and editors from magazines, newspapers, trade
                   publications and wire services.
                   </li>

              <li> <strong>Television and Radio</strong>:
                   Broadcast reporters and producers from news organizations.
                   </li>

              <li> <strong>Press or Industry Analyst</strong>
                   </li>

              <li> <strong>Freelance Writer</strong>
                   </li>

              <li> <strong>Online Publications</strong>:
                   We do not consider personal web pages and blogs to be
                   'online publications' for the purposes of issuing
                   media credentials.
                   </li>

              <li> <strong>University Publications</strong>:
                   University publications are limited to
                   one media representative.
                   </li>
            </ul>

